#include <stdio.h>
#include <stdlib.h>
/*Codigo que esxplica de forma practica como funciona el incremento y decremento*/
int main(){
    int c = 5;
    printf("c : %d \n",c);
    printf("c++ : %d \n",c++);
    printf("c : %d \n",c);

    int d= 10;
    printf("d : %d \n",d);
    printf("--d : %d \n",--d);
    printf("d : %d \n",d);
    return 0;

}
